    // Shows instructors from database to tableInstructor
    public void showInstructorsInTable() {
        ArrayList<Instructor> list = app2.getInstructorsList();
        DefaultTableModel model = (DefaultTableModel)tableInstructor.getModel();
        Object[] row = new Object[6];
        for(int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getPersonId();
            row[1] = list.get(i).getName();
            row[2] = list.get(i).getEmployeeNumber();
            row[3] = list.get(i).getWeb();
            row[4] = list.get(i).getPhoneNumber();
            row[5] = list.get(i).getSalary();
            
            model.addRow(row);
        }
    }
    
    // Shows autoCourse from database to tableAutoCourse
    public void showAutoCourseInTable() {
        ArrayList<AutoCourse> list = app2.getAutoCoursesList();
        DefaultTableModel model = (DefaultTableModel)tableAutoCourse.getModel();
        Object[] row = new Object[5];
        for(int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getCode();
            row[2] = list.get(i).getDescription();
            row[3] = list.get(i).getDuration();
            row[4] = list.get(i).getCost();
            
            model.addRow(row);
        }
    }
    
    // Shows courses id from selected instructor 
    public List<AutoCourse> showRelationshipInTable(List<AutoCourse> acList) {
        DefaultTableModel model = (DefaultTableModel)tableRelationship.getModel();
        Object[] row = new Object[1];
        for(int i = 0; i < acList.size(); i++) {
            row[0] = acList.get(i).getId();
            model.addRow(row);
        }  
        return acList;
    }
    
    //Check if integer
    public static boolean isInteger(String s) {
        boolean isValidInteger = false;
        try
        {
            Integer.parseInt(s);
 
            isValidInteger = true;
        }
        catch (NumberFormatException ex)
        {
            return isValidInteger;
        }
 
        return isValidInteger;
    }
