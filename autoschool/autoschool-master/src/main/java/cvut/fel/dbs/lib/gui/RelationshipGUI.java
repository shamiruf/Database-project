    // Button read shows all courses from selected instructor
    private void bttnReadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnReadActionPerformed
        app2.setup();
        if(tfInstructorIdRead.getText().trim().isEmpty() || !isInteger(String.valueOf(tfInstructorIdRead.getText()))) {
            errorLabelRead.setText("Instructor id isn't correct.");
            errorLabelRead.setVisible(true);
        } else {
            try{
                errorLabelRead.setVisible(false);
                Long personId = Long.valueOf(tfInstructorIdRead.getText());
            
                Instructor ins = app2.findIns(personId);
            
                if(ins.getPersonId() == null){
                    errorLabelRead.setText("Didn't find instructor.");
                    errorLabelRead.setVisible(true);
                    DefaultTableModel model = (DefaultTableModel)tableRelationship.getModel();
                    model.setRowCount(0);
                } else {
                
                    app2.getTransactionBegin();
                    List<AutoCourse> acList = ins.getInstructoredAutoCourses();
                    app2.getTransactionCommit();
                
                    DefaultTableModel model = (DefaultTableModel)tableRelationship.getModel();
                    model.setRowCount(0);
                    showRelationshipInTable(acList);
                }
            } catch (Exception ex) {
                System.out.print(ex);
            } finally {
                app2.close();
            }
        }
    }//GEN-LAST:event_bttnReadActionPerformed

    // Button Create - create relatiionship (instructor, autocourse)
    private void bttnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnCreateActionPerformed
        app2.setup();
        if(tfInstructorId.getText().trim().isEmpty() || !isInteger(String.valueOf(tfInstructorId.getText()))) {
            errorLabel.setText("Instructor id isn't correct.");
            errorLabel.setVisible(true);
        } else if (tfAutoCourseId.getText().trim().isEmpty() || !isInteger(String.valueOf(tfAutoCourseId.getText()))){
            errorLabel.setText("AutoCourse id isn't correct.");
            errorLabel.setVisible(true);
        } else {
            try{
                errorLabel.setVisible(false);
                errorLabelRead.setVisible(false);
                Long personId = Long.valueOf(tfInstructorId.getText());
                Long acId = Long.valueOf(tfAutoCourseId.getText());
                Instructor ins = app2.findIns(personId);
                AutoCourse ac = app2.findAutoCourse(acId);
                if(ins.getPersonId() == null || ac.getId() == null){
                    errorLabel.setText("Didn't find instructor or course.");
                    errorLabel.setVisible(true);
                } else {
                    errorLabel.setVisible(false);
                    Boolean b = app2.addAutoCourseToInstructor(ins, ac);
                    if (b == false) {
                        errorLabel.setText("Already exists.");
                        errorLabel.setVisible(true);
                    } else {
                        errorLabel.setVisible(false);
                    }
                }
            }catch (Exception ex) {
                System.out.print(ex);
            } finally {
                app2.close();
            }
        }
        
    }//GEN-LAST:event_bttnCreateActionPerformed
    
    // Button Delete - delete relatiionship (instructor, autocourse)
    private void bttnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnDeleteActionPerformed
        app2.setup();
        if(tfInstructorId.getText().trim().isEmpty()|| !isInteger(String.valueOf(tfInstructorId.getText()))) {
            errorLabel.setText("Instructor id isn't correct.");
            errorLabel.setVisible(true);
        } else if (tfAutoCourseId.getText().trim().isEmpty() || !isInteger(String.valueOf(tfAutoCourseId.getText()))){
            errorLabel.setText("AutoCourse id isn't correct.");
            errorLabel.setVisible(true);
        } else {
            try{
                errorLabel.setVisible(false);
                errorLabelRead.setVisible(false);
                Long personId = Long.valueOf(tfInstructorId.getText());
                Long acId = Long.valueOf(tfAutoCourseId.getText());
                Instructor ins = app2.findIns(personId);
                AutoCourse ac = app2.findAutoCourse(acId);
                if(ins.getPersonId() == null || ac.getId() == null){
                    errorLabel.setText("Didn't find instructor or course.");
                    errorLabel.setVisible(true);
                } else {
                    errorLabel.setVisible(false);
                    Boolean b = app2.deleteAutoCourseFromInstructor(ins, ac);
                    if (b == false) {
                        errorLabel.setText("Doesn't exist.");
                        errorLabel.setVisible(true);
                    } else {
                        errorLabel.setVisible(false);
                    }
                }
            }catch (Exception ex) {
                System.out.print(ex);
            } finally {
                app2.close();
            }
        }
    }//GEN-LAST:event_bttnDeleteActionPerformed

